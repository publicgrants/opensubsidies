// =============================================================================
// Favicon auto-fill — derives a `faviconUrl` for every funder from its
// `website` host using Google's public S2 favicon service. This is the same
// CDN browsers and tools like Slack/Notion already hit; no secrets or API
// keys are involved. Funders that already specify `faviconUrl` keep theirs.
// =============================================================================
function hostnameOf(url: string): string {
  try {
    return new URL(url).hostname.replace(/^www\./, "");
  } catch {
    return "";
  }
}

/**
 * Build a S2-favicon URL for the given site. We request size 128 because the
 * watermark renders at 15rem × 15rem inside the card backdrop.
 */
export function faviconFor(websiteOrHost: string, size = 128): string {
  const host = websiteOrHost.includes("://")
    ? hostnameOf(websiteOrHost)
    : websiteOrHost;
  if (!host) return "";
  return `https://www.google.com/s2/favicons?domain=${host}&sz=${size}`;
}

for (const f of funders) {
  if (!f.faviconUrl) {
    f.faviconUrl = faviconFor(f.website, 128);
  }
}

