// =============================================================================
// Grant.com — Global Grant Intelligence Seed Data
// =============================================================================
// This file is the source-of-truth seed dataset for the Grant.com landing
// dashboard. It models real-world public grant programmes from European,
// North American, Nordic, Asian and multilateral funders. The data is shaped
// to mirror what the production ingestion pipeline (Virkely → Grant.com)
// surfaces: funder, jurisdiction, scheme metadata, allocation history,
// eligibility, deadlines and budget envelopes.
//
// File path is preserved (`mock-data/locations.ts`) for compatibility with
// the underlying maps template plumbing — but the exported domain is now
// grants. The exports below are imported as `Grant`, `categories` (sectors),
// `tags` (instruments / grant types) and `funders` across the dashboard.
// =============================================================================

export type Sector = {
  id: string;
  name: string;
  icon: string; // lucide icon key
  color: string; // hex
  description: string;
};

export type GrantTag = {
  id: string;
  name: string;
  description?: string;
};

export type FunderType =
  | "supranational"
  | "national"
  | "regional"
  | "agency"
  | "foundation"
  | "multilateral";

export type Funder = {
  id: string;
  name: string;
  shortName: string;
  type: FunderType;
  country: string; // ISO-3166 alpha-2 (or "EU", "GLOBAL")
  countryName: string;
  flag: string; // emoji flag
  hq: string; // city
  website: string;
  /**
   * Public favicon URL for the funder, auto-derived from `website` via
   * Google's S2 favicon service (`https://www.google.com/s2/favicons?...`)
   * unless overridden below. No secrets / API keys involved — it is a
   * public CDN endpoint used by browsers, Slack, Notion, etc. Renders
   * inside the GrantCard backdrop the same way dirstarter renders
   *   <Favicon src={grantProvider.faviconUrl} />.
   */
  faviconUrl?: string;
};

export type Allocation = {
  year: number;
  totalAwardedEUR: number;
  awardCount: number;
  applicants?: number;
};

export type GrantStatus = "open" | "upcoming" | "closing-soon" | "closed";

export type Grant = {
  id: string;
  name: string;
  funderId: string;
  description: string;
  // For map: funder HQ or country centroid
  coordinates: { lat: number; lng: number };
  address: string; // funder HQ city/address line — kept for compatibility
  // Scheme metadata
  sectorId: string;
  tags: string[];
  status: GrantStatus;
  publishedAt: string; // ISO date
  openingDate: string; // ISO date
  deadline: string; // ISO date — for "closing-soon" / countdown
  fundingMinEUR: number;
  fundingMaxEUR: number;
  totalAnnualBudgetEUR: number;
  cofinancingRate: number; // 0-100, % of project covered
  fundingType: "grant" | "blended" | "loan" | "equity" | "tax-credit" | "prize";
  // Eligibility / scope
  eligibleEntities: Array<
    "startup" | "sme" | "corporate" | "university" | "ngo" | "public" | "individual"
  >;
  geographicScope: string[]; // ISO codes of eligible applicant countries, or ["EU"], ["GLOBAL"]
  geographicScopeLabel: string; // human-readable
  // Application
  applicationUrl: string;
  // History & insights
  allocations: Allocation[];
  matchScore: number; // 0-100, AI-suggested fit
  awardCountLastCycle: number;
  successRate: number; // % of applicants funded last cycle
  notableAwardees: string[];
  requirements: string[];
  // UI state
  isSaved: boolean; // formerly isFavorite
  viewCount: number; // formerly visitCount
  lastViewed?: string;
  // Compatibility shims for existing template UI surfaces:
  // - `rating` ≈ matchScore/20 (0–5) for star-style fit display
};

